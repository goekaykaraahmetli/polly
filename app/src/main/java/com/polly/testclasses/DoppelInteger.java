package com.polly.testclasses;

public class DoppelInteger {
	private final int intEins;
	private final int intZwei;
	
	public DoppelInteger(int intEins, int intZwei) {
		this.intEins = intEins;
		this.intZwei = intZwei;
	}

	public int getIntEins() {
		return intEins;
	}

	public int getIntZwei() {
		return intZwei;
	}
}
